CREATE TABLE `lbe_apkrepo` (
  `file_md5` VARCHAR(32) NOT NULL,
  `file_name` VARCHAR(256) NOT NULL,
  `file_size` INT(11) NOT NULL,
  `package_name` VARCHAR(128),
  `cert_md5` VARCHAR(32) ,
  `cert_text` VARCHAR(4096),
  `sf_md5` VARCHAR(32) ,
  `mf_md5` VARCHAR(32) ,
  `dex_digest_method` VARCHAR(32),
  `dex_digest` VARCHAR(64),
  `axml_digest_method` VARCHAR(32),
  `axml_digest` VARCHAR(64),
  `arsc_digest_method` VARCHAR(32),
  `arsc_digest` VARCHAR(64),
  `version_name` VARCHAR(128),
  `version_code` INT,
  `min_sdk_ver` INT,
  `target_sdk_ver` INT,
  `permissions` VARCHAR(4096),
  `activities` VARCHAR(4096),
  `services` VARCHAR(1024),
  `receivers` VARCHAR(1024),
  `providers` VARCHAR(1024),
  `libraries` VARCHAR(1024),
  `intentfilters` VARCHAR(2048),
  PRIMARY KEY (`file_md5`)  
) ENGINE=MYISAM DEFAULT CHARSET=utf8